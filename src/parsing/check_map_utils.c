#include "../cub3d.h"

int	is_allowed_all(char c)
{
	return (c == 'N' || c == 'W' || c == 'E' \
			|| c == 'S' || c == '1' || c == '0' || c == ' ');
}

int	is_allowed_p(char c)
{
	return (c == 'N' || c == 'W' || c == 'E' || c == 'S');
}
